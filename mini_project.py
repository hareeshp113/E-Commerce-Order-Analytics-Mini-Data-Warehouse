import pandas as pd

def extract():
  orders_df = pd.read_csv('raw_orders_project.csv')
  customers_df = pd.read_csv('raw_customers.csv')
  products_df = pd.read_csv('raw_products.csv')
  return orders_df, customers_df, products_df

def clean_customers(customers_df):
    """Clean the customers dataset and add age_group."""
    customers_df = customers_df.copy()

    # Standardize column names
    customers_df.columns = [col.strip().lower() for col in customers_df.columns]

    # Remove duplicate customer rows
    customers_df = customers_df.drop_duplicates()

    # Fill missing email with a placeholder value
    customers_df["email"] = customers_df["email"].fillna(
        customers_df["customer_name"].str.lower().str.replace(" ", "_") + "@unknown.com"
    )

    # Fill missing age with median age
    median_age = round(customers_df["age"].median())
    customers_df["age"] = customers_df["age"].fillna(median_age).astype(int)

    # Fill missing city with Unknown
    customers_df["city"] = customers_df["city"].fillna("Unknown")

    # Create age_group column
    def get_age_group(age):
        if age < 30:
            return "Under 30"
        elif age <= 40:
            return "30 to 40"
        return "Over 40"

    customers_df["age_group"] = customers_df["age"].apply(get_age_group)

    return customers_df

def clean_products(products_df):

  products_df = products_df.copy()
  # standarized the columns
  products_df.columns = [col.strip().lower() for col in products_df]

  #remove the duplicates
  products_df = products_df.drop_duplicates()

  #Fill Missing Category
  products_df['category'] = products_df['category'].fillna('Electronics')

  #Convert product names inti title case
  products_df['product_name'] = products_df['product_name'].str.title()

  return products_df

def transform(orders_df, customers_df, products_df):

  orders_df = orders_df.copy()

  #standarized the columns
  orders_df.columns = [col.strip().lower() for col in orders_df]

  #Remove the duplicates
  orders_df = orders_df.drop_duplicates()

  #Fill the missing the customer id
  orders_df = orders_df.dropna(subset=['customer_id'])

  #Fill the missing order_date
  orders_df = orders_df.dropna(subset=['order_date'])

  #Fill missing unit price(important below part)
  # Remove duplicate product rows
  products_df = products_df.drop_duplicates(subset=["product_id"])

  # Create product_id -> unit_price lookup
  product_price_map = products_df.set_index("product_id")["unit_price"]

  # Fill missing unit_price using lookup
  orders_df["unit_price"] = orders_df["unit_price"].fillna(
    orders_df["product_id"].map(product_price_map))

  # Converting numerical columns
  orders_df['quantity'] = orders_df['quantity'].astype(int)
  orders_df["unit_price"] = orders_df["unit_price"].astype(float)
  orders_df["discount"] = orders_df["discount"].astype(float)

  # Add total_amount column
  orders_df["total_amount"] = (
      orders_df["quantity"] * orders_df["unit_price"]) - orders_df["discount"]

  return orders_df

def load(orders_df, customers_df, products_df):
  orders_df.to_csv("stg_orders.csv", index=False)
  customers_df.to_csv("stg_customers.csv", index=False)
  products_df.to_csv("stg_products.csv", index=False)

def main():
    orders_df, customers_df, products_df = extract()

    customers_clean = clean_customers(customers_df)
    products_clean = clean_products(products_df)

    orders_clean = transform(
        orders_df,
        customers_clean,
        products_clean
    )

    load(
        orders_clean,
        customers_clean,
        products_clean
    )

    print("ETL completed successfully.")

if __name__ == "__main__":
    main()

