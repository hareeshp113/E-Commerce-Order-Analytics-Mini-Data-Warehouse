"""#BUSINESS QUESTIONS
##1. Which customer spent the most in total?
"""

query1 = """
SELECT
    c.customer_id,
    c.customer_name,
    ROUND(SUM(f.total_amount), 2) AS total_spent
FROM fact_orders f
JOIN dim_customers c
    ON f.customer_id = c.customer_id
GROUP BY c.customer_id, c.customer_name
ORDER BY total_spent DESC
LIMIT 1
"""

print(pd.read_sql(query1, conn))

"""##2.Which product generates the highest total revenue?"""

query2 = """
SELECT
    p.product_id,
    p.product_name,
    ROUND(SUM(f.total_amount), 2) AS total_revenue
FROM fact_orders f
JOIN dim_products p
    ON f.product_id = p.product_id
GROUP BY p.product_id, p.product_name
ORDER BY total_revenue DESC
LIMIT 1
"""

print(pd.read_sql(query2, conn))

"""##3.How does revenue trend week by week across January?"""

query3 = """
SELECT
    strftime('%W', d.full_date) AS week_number,
    ROUND(SUM(f.total_amount), 2) AS weekly_revenue
FROM fact_orders f
JOIN dim_date d
    ON f.date_id = d.date_id
WHERE d.month = 1
  AND d.year = 2024
GROUP BY strftime('%W', d.full_date)
ORDER BY week_number
"""

print(pd.read_sql(query3, conn))

"""##4.Which store has the highest average order value?"""

query4 = """
SELECT
    store_id,
    ROUND(AVG(total_amount), 2) AS avg_order_value
FROM fact_orders
GROUP BY store_id
ORDER BY avg_order_value DESC
LIMIT 1
"""

print(pd.read_sql(query4, conn))

