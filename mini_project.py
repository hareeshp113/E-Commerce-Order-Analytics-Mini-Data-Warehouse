"""##CREATE STAR SCHEMA

###Create dim_customers
"""

cursor.execute("DROP TABLE IF EXISTS dim_customers")

cursor.execute("""
CREATE TABLE dim_customers AS
SELECT DISTINCT
    customer_id,
    customer_name,
    city,
    country,
    age_group
FROM stg_customers
""")

cursor.execute("DROP TABLE IF EXISTS dim_products")

cursor.execute("""
CREATE TABLE dim_products AS
SELECT DISTINCT
    product_id,
    product_name,
    category,
    sub_category,
    brand
FROM stg_products
""")

cursor.execute("DROP TABLE IF EXISTS dim_date")

cursor.execute("""
CREATE TABLE dim_date AS
SELECT DISTINCT
    CAST(strftime('%Y%m%d', order_date) AS INTEGER) AS date_id,
    order_date AS full_date,
    CAST(strftime('%Y', order_date) AS INTEGER) AS year,
    CAST(strftime('%m', order_date) AS INTEGER) AS month,
    CASE strftime('%m', order_date)
        WHEN '01' THEN 'January'
        WHEN '02' THEN 'February'
        WHEN '03' THEN 'March'
        WHEN '04' THEN 'April'
        WHEN '05' THEN 'May'
        WHEN '06' THEN 'June'
        WHEN '07' THEN 'July'
        WHEN '08' THEN 'August'
        WHEN '09' THEN 'September'
        WHEN '10' THEN 'October'
        WHEN '11' THEN 'November'
        WHEN '12' THEN 'December'
    END AS month_name,
    CASE
        WHEN CAST(strftime('%m', order_date) AS INTEGER) BETWEEN 1 AND 3 THEN 'Q1'
        WHEN CAST(strftime('%m', order_date) AS INTEGER) BETWEEN 4 AND 6 THEN 'Q2'
        WHEN CAST(strftime('%m', order_date) AS INTEGER) BETWEEN 7 AND 9 THEN 'Q3'
        ELSE 'Q4'
    END AS quarter,
    CASE strftime('%w', order_date)
        WHEN '0' THEN 'Sunday'
        WHEN '1' THEN 'Monday'
        WHEN '2' THEN 'Tuesday'
        WHEN '3' THEN 'Wednesday'
        WHEN '4' THEN 'Thursday'
        WHEN '5' THEN 'Friday'
        WHEN '6' THEN 'Saturday'
    END AS day_of_week,
    CASE
        WHEN strftime('%w', order_date) IN ('0', '6') THEN 'Yes'
        ELSE 'No'
    END AS is_weekend
FROM stg_orders
""")

cursor.execute("DROP TABLE IF EXISTS fact_orders")

cursor.execute("""
CREATE TABLE fact_orders AS
SELECT
    order_id,
    customer_id,
    product_id,
    CAST(strftime('%Y%m%d', order_date) AS INTEGER) AS date_id,
    store_id,
    quantity,
    unit_price,
    discount,
    total_amount
FROM stg_orders
""")

